package main

import (
	"fmt"
	"sort"
	"strconv"
	"sync"
)

func threeSum(nums []int) [][]int {
	var res [][]int
	sumMap := make(map[int][][2]int)
	for i := 0; i < len(nums)-1; i++ {
		for j := i + 1; j < len(nums); j++ {
			sumMap[nums[i]+nums[j]] = append(sumMap[nums[i]+nums[j]], [2]int{i, j})
		}
	}

	var mu sync.Mutex
	strSet := make(map[string]struct{})
	var wg sync.WaitGroup

	for i := 0; i < len(nums); i++ {
		wg.Add(1)
		go func(i int) {
			
			defer wg.Done()
			complement := -nums[i]
			if pairs, found := sumMap[complement]; found {
				for _, p := range pairs {
					if i == p[0] || i == p[1] {
						continue
					}
					triplet := []int{nums[i], nums[p[0]], nums[p[1]]}
					sort.Ints(triplet)
					tripletStr := strconv.Itoa(triplet[0]) + "_" + strconv.Itoa(triplet[1]) + "_" + strconv.Itoa(triplet[2])
					mu.Lock()
					// fmt.Println(i, "get the lock") // TODO debug 2
					if _, found := strSet[tripletStr]; !found {
						res = append(res, triplet)
						strSet[tripletStr] = struct{}{}
					}
					// fmt.Println(i, "release the lock") // TODO debug 3
					mu.Unlock()
				}
			}
			fmt.Println("debug", i) // TODO debug1
		}(i)
	}

	wg.Wait()
	return res
}

